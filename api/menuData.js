import client from '../utils/sample_data/client';

const endpoint = client.databaseURL;

const getMenuItems = (uid) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/menuItems.json?orderBy="uid"&equalTo="${uid}"`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});

const updateMenuItems = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/menuItems/${payload.firebaseKey}.json`, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then(resolve)
    .catch(reject);
});

const createMenuItems = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/menuItems.json`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

export { getMenuItems, updateMenuItems, createMenuItems };
